  mekanism_cardboard_blacklist.forEach((block) => {
    event.add("mekanism:cardboard_blacklist", block);
    event.add("justdirethings:swapper_deny", block);
    event.add("buildinggadgets2:deny", block);
  });

  relocationDenyBlocks.forEach((block) => {
    event.add("relocation:deny", block)
    event.add("buildinggadgets2:deny", block)
    event.add("justdirethings:eclipsegate_deny", block)
    event.add("justdirethings:paradox_absorb_deny", block)
    event.add("arsnouveau:rewind_blacklist", block)
    event.add("arsnouveau:break_blacklist", block)
    event.add("justdirethings:swapper_deny", block)
    event.add("c:relocation_not_supported", block)
    event.add("justdirethings:eclipsegate_deny", block)
    event.add("industrialforegoingsouls:cant_accelerate", block)
  })

  factoryBlocks.forEach((factoryBlock) => {
    event.add("minecraft:mineable/pickaxe", factoryBlock);
  });

  mutandis_blacklist.forEach((block) => {
    event.add("enchanted:mutandis_blacklist", block);
    event.add("enchanted:mutandis_extremis_blacklist", block);
  });

  event.add(
    "minecraft:mineable/pickaxe", [
    "mysticalagriculture:awakened_supremium_growth_accelerator",  //Temp Tag addition until its fixed by the Mystical Agriculture Dev
    "ftbstuff:blocks/creative_low_temperature_source",
    "ftbstuff:blocks/creative_high_temperature_source",
    "ftbstuff:blocks/creative_subzero_temperature_source"
    ]
  );

  //Why Forbidden Arcanus is so broken? Adding a Tag so this block can be properly broken.
